package com.example.appretrofitgetpost;

public class Meta {
    private String status;
    private int today_request_count;
    private double balance;

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public int getToday_request_count() {
        return today_request_count;
    }

    public void setToday_request_count(int today_request_count) {
        this.today_request_count = today_request_count;
    }

    public double getBalance() {
        return balance;
    }

    public void setBalance(double balance) {
        this.balance = balance;
    }

}
